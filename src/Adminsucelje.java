    private void popuniGradove(){
        combograd.removeAllItems();
        String zupanija=combozupanija.getSelectedItem().toString();
        
        ResultSet grad=Metode.DajGradoveOdZupanija(zupanija);
        try {
            while(grad.next()){
                 combograd.addItem(grad.getString("nazivgrada"));
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
     
        
    }

    private void spremiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_spremiActionPerformed
        Djelatnik d= new  Djelatnik();
        
        d.setIme(textime.getText());
        d.setPrezime(textprezime.getText());
        d.setAdresa(textadresa.getText());
        d.setTelefon(texttelefon.getText());
        d.setMobitel(textmobitel.getText());
        d.setEmail(textemail.getText());
        d.setGrad(combograd.getSelectedItem().toString());
        d.setZupanija(combozupanija.getSelectedItem().toString());
        d.setPoslovnica(comboposlovnica.getSelectedItem().toString());
        d.setStatus(textstatus.getText());
        d.setUzer(textuzer.getText());
        d.setPass(textpass.getText());
        
        JOptionPane opt=new JOptionPane();
        
        if(d.valjaniPodaci()){
            boolean uspjesno=Metode.spremiDjelatnika(d);
            
            if(uspjesno){
                
                opt.showMessageDialog(this,"Uspješno snimljeno","OBAVIJEST",JOptionPane.INFORMATION_MESSAGE);
                reset();
                Metode.dajSveDjelatnike();
            }else{
                opt.showMessageDialog(this,"Nije snimljeno","POZOR",JOptionPane.INFORMATION_MESSAGE);
            }
        }else{
            opt.showMessageDialog(this,"Molim upišite ime i prezime","POZOR",JOptionPane.INFORMATION_MESSAGE);
        }
        
        
        
        
        
        
    }//GEN-LAST:event_spremiActionPerformed


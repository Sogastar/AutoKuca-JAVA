    private void popuniZupanije(){
        combozupanija.removeAllItems();
        ResultSet zupanija=Metode.DajZupanije();
        try {
            while(zupanija.next()){
                combozupanija.addItem(zupanija.getString("nazivzupanije"));
                
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    } 
     private void popuniPoslovnice(){
        comboposlovnica.removeAllItems();
        ResultSet poslovnica=Metode.dajSvePoslovnice();
        try {
            while(poslovnica.next()){
                comboposlovnica.addItem(poslovnica.getString("nazivposlovnice"));
                
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    } 
    
    

     
    
    
